# not 연산자 : 불을 반대로 전환한다.
print(not True)
print(not False) 
print()

#and 연산자 : 피연산자 두 개가 모두 참일 때 True를 출력한다
print(True and True) 
print(True and False)
print(False and True)
print(False and False)
print()

#or 연산자 : 피연산자 둘 중 한개만 참일 때 True를 출력한다
print(True or True) 
print(True or False)
print(False or True)
print(False or False)
