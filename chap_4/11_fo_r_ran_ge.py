 #콜론은 꼭 붙혀줍시다.
for i in range(10):
    print(str(i)+"번째 반복입니다.")
print()

#range(5)에 해당하는 [0,1,2,3,4]가 반복됩니다.
for i in range(5): 
    print(str(i)+"= 반복 변수")
print()

#range(5,10) 5에서 9까지 반복됩니다.
for i in range(5,10): 
    print(str(i)+"= 반복 변수")
print()

#range(0,10,3) 0에서 9까지 3칸씩 반복됩니다.
for i in range(0,10,3): 
    print(str(i)+"= 반복 변수")
print()


    